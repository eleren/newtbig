package utils

func Cause(err error) error {
	type causer interface {
		Cause() error
	}

	for err != nil {
		cause, ok := err.(causer)
		if !ok {
			break
		}
		err = cause.Cause()
	}
	return err
}

type timeoutError interface {
	Timeout() bool
}

func IsTimeoutError(err error) bool {
	if err == nil {
		return false
	}
	err = Cause(err)
	ne, ok := err.(timeoutError)
	return ok && ne.Timeout()
}
